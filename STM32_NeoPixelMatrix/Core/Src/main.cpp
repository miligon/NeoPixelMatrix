void LoadData(Matriz& matrix, unsigned char *data){
	int r,g,b;
	double y = 0.0;
	for(int i = 0; i<ROWS; i++){
		for(int j = 0; j<COLS; j++){
			r = data[(((i*ROWS) + j) * 4)+0];
			g = data[(((i*ROWS) + j) * 4)+1];
			b = data[(((i*ROWS) + j) * 4)+2];
			y = data[(((i*ROWS) + j) * 4)+3]/100.0;
			matrix.pixel(i, j).SetRGB(r,g,b);
			matrix.pixel(i, j).SetBrillo(y);
		}
	}
}
